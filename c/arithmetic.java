class arithmetic{
	public static void main(String args[]){
		int a=5,b=10,c=0;
		c=a+b;
		System.out.println("Sum= "+c);
		c=a-b;
		System.out.println("Subtraction= "+c);
		c=a*b;
		System.out.println("Multiplication= "+c);
		c=a/b;
		System.out.println("Division= "+c);
		c=a%b;
		System.out.println("Modulus= "+c);
	}
}